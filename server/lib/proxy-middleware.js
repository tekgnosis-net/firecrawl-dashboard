import crypto from 'crypto';
import { classifyOperation } from './classifier.js';
import { parseFirecrawlResponse, extractRequestFields } from './response-parser.js';
import { forwardToFirecrawl, filterHeaders } from './firecrawl-client.js';
import {
  getProxyMaxBodyBytes, getProxyTrustForwardedFor, getDebugLogBodies,
} from './settings.js';

/**
 * Proxy middleware factory. Returns an Express-compatible (req, res)
 * handler that forwards to Firecrawl and enqueues an observation row.
 *
 * Every inbound request hits this single handler regardless of route.
 * The handler:
 *   1. Measures start time
 *   2. Resolves client identity (IP, UA, auth hash)
 *   3. Extracts request-body fields (target_url, query, size)
 *   4. Classifies operation type
 *   5. Forwards via firecrawl-client.forwardToFirecrawl
 *   6. Parses response body for stats fields
 *   7. Enqueues row onto write queue (non-blocking)
 *   8. Forwards response headers + body to client
 *
 * On upstream failure (ECONN, timeout, payload-too-large) the row is
 * still enqueued with response_status=0 and an error_message, and the
 * client sees a 502/504 with a structured error envelope mirroring
 * Firecrawl's own.
 */

const MAX_UA_LENGTH = 256;
const MAX_ERROR_LENGTH = 1000;

function sha256Hex(input) {
  return crypto.createHash('sha256').update(input).digest('hex');
}

function hashBearerToken(authorization) {
  if (!authorization) return null;
  const match = authorization.match(/^Bearer\s+(.+)$/i);
  if (!match) return null;
  return sha256Hex(match[1]);
}

function resolveClientIp(req, trustXff) {
  if (trustXff) {
    const xff = req.headers['x-forwarded-for'];
    if (typeof xff === 'string' && xff.length > 0) {
      // RFC 7239: leftmost entry is the original client
      return xff.split(',')[0].trim();
    }
  }
  // Express req.ip resolves from connection.remoteAddress, which may
  // include an IPv6-mapped prefix like ::ffff:127.0.0.1 — strip it.
  const ip = req.ip || req.connection?.remoteAddress || null;
  return ip && ip.startsWith('::ffff:') ? ip.substring(7) : ip;
}

function filterResponseHeaders(headers) {
  // Use the same hop-by-hop filter as request headers, plus ensure
  // content-encoding and content-length get regenerated by Express.
  const filtered = filterHeaders(headers);
  delete filtered['content-encoding'];
  delete filtered['content-length'];
  return filtered;
}

function extractIdFromCrawlPath(pathOnly) {
  const match = pathOnly.match(/^\/v[12]\/crawl\/([^/?]+)/);
  return match ? match[1] : null;
}

export function createProxyMiddleware(db, writeQueue) {
  return async function proxyMiddleware(req, res) {
    const startNs = process.hrtime.bigint();
    const timestamp = new Date().toISOString();

    // Read hot-path settings fresh from DB (sub-ms each)
    const maxBodyBytes = getProxyMaxBodyBytes(db);
    const trustXff = getProxyTrustForwardedFor(db);
    const logBodies = getDebugLogBodies(db);

    // Client identity
    const clientIp = resolveClientIp(req, trustXff);
    const clientUa = (req.headers['user-agent'] || '').substring(0, MAX_UA_LENGTH) || null;
    const clientAuthHash = hashBearerToken(req.headers['authorization']);

    // Operation classification
    const pathOnly = req.originalUrl.split('?')[0];
    const operationType = classifyOperation(req.method, pathOnly);

    // Extract request-body fields
    const { targetUrl, targetHost, queryText } = extractRequestFields(operationType, req.body);
    const requestBodyJson = req.body ? JSON.stringify(req.body) : '';
    const requestBodySize = Buffer.byteLength(requestBodyJson, 'utf8');

    // Forward to Firecrawl
    let upstream;
    let upstreamError = null;
    try {
      upstream = await forwardToFirecrawl(db, req, { maxBodyBytes, timeoutMs: 120_000 });
    } catch (err) {
      upstreamError = err;
    }

    const durationMs = Number((process.hrtime.bigint() - startNs) / 1_000_000n);

    // Build log row
    let row;
    if (upstreamError) {
      row = {
        timestamp,
        method: req.method,
        path: pathOnly,
        operation_type: operationType,
        client_ip: clientIp,
        client_ua: clientUa,
        client_auth_hash: clientAuthHash,
        target_url: targetUrl,
        target_host: targetHost,
        query_text: queryText,
        request_body_size: requestBodySize,
        response_status: 0,
        response_size: 0,
        success: 0,
        error_message: (upstreamError.message || 'Upstream error').substring(0, MAX_ERROR_LENGTH),
        firecrawl_id: null,
        credits_used: null,
        scraped_status_code: null,
        concurrency_limited: null,
        duration_ms: durationMs,
        upstream_response_time_ms: null,
        request_body: logBodies ? requestBodyJson.substring(0, 1_000_000) : null,
        response_body: null,
      };
      writeQueue.enqueue(row);

      // Return a structured error to the client
      let status = 502;
      let code = 'UPSTREAM_ERROR';
      if (upstreamError.kind === 'timeout') { status = 504; code = 'UPSTREAM_TIMEOUT'; }
      if (upstreamError.kind === 'payload_too_large') { status = 502; code = 'PAYLOAD_TOO_LARGE'; }
      return res.status(status).json({
        success: false,
        code,
        error: upstreamError.message || 'Firecrawl unreachable',
      });
    }

    // Parse response body for log fields
    const parsed = parseFirecrawlResponse(operationType, upstream.body, upstream.status);

    // For crawl_status and crawl_cancel, firecrawl_id comes from the path
    if (operationType === 'crawl_status' || operationType === 'crawl_cancel') {
      const idFromPath = extractIdFromCrawlPath(pathOnly);
      if (idFromPath) parsed.firecrawlId = idFromPath;
    }

    row = {
      timestamp,
      method: req.method,
      path: pathOnly,
      operation_type: operationType,
      client_ip: clientIp,
      client_ua: clientUa,
      client_auth_hash: clientAuthHash,
      target_url: targetUrl,
      target_host: targetHost,
      query_text: queryText,
      request_body_size: requestBodySize,
      response_status: upstream.status,
      response_size: upstream.body.length,
      success: parsed.success,
      error_message: parsed.errorMessage ? parsed.errorMessage.substring(0, MAX_ERROR_LENGTH) : null,
      firecrawl_id: parsed.firecrawlId,
      credits_used: parsed.creditsUsed,
      scraped_status_code: parsed.scrapedStatusCode,
      concurrency_limited: parsed.concurrencyLimited,
      duration_ms: durationMs,
      upstream_response_time_ms: upstream.upstreamResponseTimeMs ? Math.round(upstream.upstreamResponseTimeMs) : null,
      request_body: logBodies ? requestBodyJson.substring(0, 1_000_000) : null,
      response_body: logBodies ? upstream.body.toString('utf8').substring(0, 1_000_000) : null,
    };
    writeQueue.enqueue(row);

    // Forward response to client
    res.status(upstream.status);
    const headers = filterResponseHeaders(upstream.headers);
    for (const [key, value] of Object.entries(headers)) {
      res.setHeader(key, value);
    }
    res.send(upstream.body);
  };
}
